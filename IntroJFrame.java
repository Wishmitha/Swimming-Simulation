    // Displays the number of swimmers you can select according to chosen value
    private void NoComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_NoComboItemStateChanged
        if(NoCombo.getSelectedIndex()==1){
            SwimmerCombo1.setVisible(true);
            SwimmerCombo2.setVisible(true);
            SwimmerCombo3.setVisible(false);
            SwimmerCombo4.setVisible(false);
            SwimmerCombo5.setVisible(false);   
        }else if(NoCombo.getSelectedIndex()==2){
            SwimmerCombo1.setVisible(true);
            SwimmerCombo2.setVisible(true);
            SwimmerCombo3.setVisible(true);
            SwimmerCombo4.setVisible(false);
            SwimmerCombo5.setVisible(false);            
        }else if(NoCombo.getSelectedIndex()==3){
            SwimmerCombo1.setVisible(true);
            SwimmerCombo2.setVisible(true);
            SwimmerCombo3.setVisible(true);
            SwimmerCombo4.setVisible(true);
            SwimmerCombo5.setVisible(false); 
        }else{
            SwimmerCombo1.setVisible(true);
            SwimmerCombo2.setVisible(true);
            SwimmerCombo3.setVisible(true);
            SwimmerCombo4.setVisible(true);
            SwimmerCombo5.setVisible(true);
        }
    }//GEN-LAST:event_NoComboItemStateChanged

