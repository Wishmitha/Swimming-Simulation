    /* Following method checks all the validations (whether number of players are selected,
       whether the same person is not selected twice) and send all the information to
       controller class (SwimmingCompetetion) to sort out which players,judges are selected
       to initiate the simulation*/  
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(NoCombo.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Select number of players.");   
        }else{
                Swimming_Simulation ss = new Swimming_Simulation();
                ss.setLanes();

                Swimming_Simulation.initJudge(SwimmingCompetetion.getJudges(JudgeCombo1).get_Name(), SwimmingCompetetion.getJudges(JudgeCombo2).get_Name());
                SwimmingCompetetion.assignSeats();
                SwimmingCompetetion.assignSupportStaff();


                if(NoCombo.getSelectedIndex()==1){
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo1);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo2);
                }else if(NoCombo.getSelectedIndex()==2){
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo1);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo2);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo3);
                }else if(NoCombo.getSelectedIndex()==3){
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo1);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo2);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo3);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo4);
                }else{
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo1);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo2);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo3);
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo4); 
                    SwimmingCompetetion.setMovement(SelectBox, EventBox, SwimmerCombo5); 
                }
                
                if(condition()){
                    ScoreBoard.setTime(System.currentTimeMillis());
                    ss.hideLabel();
                    ss.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null,"Player/Judge already assigned.");
                    SwimmingCompetetion.players=new ArrayList<Swimmer>();
                }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

